#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		cmp;

	if (s1 && s2)
	{
		cmp = ft_strcmp(s1, s2);
		if (cmp == 0)
			return (1);
	}
	return (0);
}
