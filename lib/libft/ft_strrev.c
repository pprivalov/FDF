char	*ft_strrev(char *str)
{
	int		i;
	int		l;
	char	t;

	i = 0;
	l = 0;
	if (str)
	{
		while (str[l])
			l++;
		i = -1;
		while (i++ < --l)
		{
			t = str[i];
			str[i] = str[l];
			str[l] = t;
		}
		return (str);
	}
	return (0);
}
